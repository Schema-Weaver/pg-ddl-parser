CREATE UNLOGGED TABLE combo_tbl (
  id INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1),
  email TEXT NOT NULL COLLATE "C" DEFAULT 'a@b.com' CHECK (length(email) > 3) UNIQUE,
  ref_id INT REFERENCES alter_base(id) ON DELETE CASCADE,
  PRIMARY KEY (id),
  UNIQUE (email),
  CHECK (id > 0)
) WITH (fillfactor = 90, autovacuum_enabled = true) TABLESPACE pg_default;

CREATE UNIQUE INDEX IF NOT EXISTS combo_idx ON combo_tbl (email COLLATE "C" ASC NULLS LAST) INCLUDE (ref_id) WITH (fillfactor = 80) WHERE email IS NOT NULL;

CREATE OR REPLACE FUNCTION combo_fn(
  IN p1 INT DEFAULT 1,
  OUT p2 TEXT,
  VARIADIC args INT[]
) RETURNS SETOF record
  LANGUAGE plpgsql
  IMMUTABLE PARALLEL SAFE
  COST 100
  SET search_path = public
  SECURITY DEFINER
AS $$
BEGIN
  p2 := 'ok';
  RETURN NEXT;
END;
$$;

CREATE CONSTRAINT TRIGGER combo_trg
  AFTER INSERT OR UPDATE OR DELETE ON combo_tbl
  DEFERRABLE INITIALLY DEFERRED
  FOR EACH ROW
  WHEN (NEW.id > 0)
  EXECUTE FUNCTION combo_fn(NEW.id);


