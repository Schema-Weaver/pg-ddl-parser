-- PG19-specific DDL — Property Graphs

CREATE PROPERTY GRAPH social_network
  NODE TABLES (
    Person LABEL person (id, name, email),
    Company LABEL company (id, name, industry)
  )
  EDGE TABLES (
    WorksAt LABEL works_at
      SOURCE Person
      DESTINATION Company
      PROPERTIES (start_date, role),
    Knows LABEL knows
      SOURCE Person
      DESTINATION Person
      PROPERTIES (since_date, strength)
  );

CREATE PROPERTY GRAPH supply_chain
  NODE TABLES (
    Supplier LABEL supplier DEFAULT LABEL vendor (id, name),
    Product LABEL product (id, sku)
  )
  EDGE TABLES (
    Supplies LABEL supplies
      SOURCE Supplier
      DESTINATION Product
      PROPERTIES (price, lead_time)
  );

